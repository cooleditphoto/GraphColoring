public class AlgorithmResult {
    int FirstFit;
    int CBIP;
    int NewAlg;

    public int getFirstFit() {
        return FirstFit;
    }

    public void setFirstFit(int firstFit) {
        FirstFit = firstFit;
    }

    public int getCBIP() {
        return CBIP;
    }

    public void setCBIP(int CBIP) {
        this.CBIP = CBIP;
    }

    public int getNewAlg() {
        return NewAlg;
    }

    public void setNewAlg(int newAlg) {
        NewAlg = newAlg;
    }
}
